multiplicao = num1 * num2
divisao = num1 / num2 
#funcao de impressão - preferi utulizar uma frase ao inves de vários comandos print para diminuir o codigo
print(f"A soma é {soma}, a subtração é {subtracao}, a multiplicação é {multiplicao} e a divisão é {divisao}") 

